/**
  * op_add - funtion add
  * @a: number one
  * @b: number two
  * Return: add between two numbers
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - sustrat function
  * @a: number one
  * @b: number two
  * Return: sustrab between two numbers
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - fucntion of multiplication
  * @a: number one
  * @b: number two
  * Return: multiplication between two numbers
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - function div
  *
  * @a: number one
  * @b: number two
  * Return: div bettweem two numbers
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - function mod
  *
  * @a: number one
  * @b: number two
  * Return: mod between two numbers
  */
int op_mod(int a, int b)
{
	return (a % b);
}
